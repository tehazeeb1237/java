class Sum
{
 public static int sum(int a,int b)
 {
   return a+b;
 }
 public static double sum(double a, double b)
 {
   return a+b;
  }
  public static void main(String[] args)
  {
    int myNum1 = sum(8, 5);
    double myNum2 = sum(4.3, 6.26);
  System.out.println("int: " + myNum1);
  System.out.println("double: " + myNum2);
  }
 }
